/*=============================================================
// 函 数 名：selt
// 功能描述：直接选择排序
// 输入参数： x 存放待排序数据的数组
//            n 数组长度
// 返 回 值：无
//==============================================================*/
void selt(x, n)
int* x;
int n;
{
    int i, j, k, t;

    for (i = 1; i < n; i++) {           /* i表示选择次数，共进行n-1次选择和交换*/
        k = i - 1;                      /* k保存当前最小元素的下标，初值为i-1*/

        for (j = i; j < n; j++) {
            if (x[j] < x[k]) {
                k = j;
            }
        }

        if (k != i - 1) {               /* 把最小元素交换到第i-1个位置*/
            t = x[i - 1];
            x[i - 1] = x[k];
            x[k] = t;
        }
    }

    return;
}
